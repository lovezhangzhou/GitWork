package com.zzl.pojo;

public class Card {
	
	private int uid;
	private String card;
	private Tuser tuser;
	
	
	public int getUid() {
		return uid;
	}
	public void setUid(int uid) {
		this.uid = uid;
	}
	public String getCard() {
		return card;
	}
	public void setCard(String card) {
		this.card = card;
	}
	public Tuser getTuser() {
		return tuser;
	}
	public void setTuser(Tuser tuser) {
		this.tuser = tuser;
	}
	
	
	
	

}
